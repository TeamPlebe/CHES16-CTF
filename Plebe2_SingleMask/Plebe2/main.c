#include <avr/io.h>
#include <stdio.h>
#include <string.h>
#include "aes-challenge.h"


int main(void)
{
	uint8_t key[16] = {0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c};
	uint8_t pt[16]  = {0x6b,0xc1,0xbe,0xe2,0x2e,0x40,0x9f,0x96,0xe9,0x3d,0x7e,0x11,0x73,0x93,0x17,0x2a};
	uint8_t ct[16]  = {0x3a,0xd7,0x7b,0xb4,0x0d,0x7a,0x36,0x60,0xa8,0x9e,0xca,0xf3,0x24,0x66,0xef,0x97};
	uint8_t tst[16];

	aes_indep_init();

	for(uint8_t i=0; i<32; i++)
	{
		memcpy(tst, pt, 16);
		aes_indep_key(key);
		aes_indep_enc(tst);

		if(memcmp(tst,ct,16))
		{
			return -1;
		}
	}
	
}

